import { encodeString, decodeString } from "../";

describe("EncodeString", () => {

    it("should test", () => {
        expect<u8[]>(encodeString('hello_world')).toStrictEqual([0x2c, 0x68, 0x65, 0x6c, 0x6c, 0x6f, 0x5f, 0x77, 0x6f, 0x72, 0x6c, 0x64]);
    });

    it("should test", () => {
        expect<string>(decodeString([0x2c, 0x68, 0x65, 0x6c, 0x6c, 0x6f, 0x5f, 0x77, 0x6f, 0x72, 0x6c, 0x64])).toStrictEqual('hello_world');
    });
});
